/* write a switch statement to print day of the week based on day number */
var day = 5;
switch (day) {
	case 5:
		console.log("friday")
		break
	case 0:
		console.log("sunday")
		break
	case 1:
		console.log("monday")
		break
	case 3:
		console.log("wednesday")
		break
	case 2:
		console.log("tuesday")
		break
	case 4:
		console.log("thursday")
		break
	case 6:
		console.log("saturday")
        break
    default:console.log("enter valid day")
}